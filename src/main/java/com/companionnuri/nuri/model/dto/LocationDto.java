package com.companionnuri.nuri.model.dto;

public class LocationDto {

    String locCategory;
    String locName;
    String locAddr;
    String locRoadAddr;
    String locTel;
    String locLat;
    String locLng;
    int locRegionCode;

    public String getLocCategory() {
        return locCategory;
    }

    public void setLocCategory(String locCategory) {
        this.locCategory = locCategory;
    }

    public String getLocName() {
        return locName;
    }

    public void setLocName(String locName) {
        this.locName = locName;
    }

    public String getLocAddr() {
        return locAddr;
    }

    public void setLocAddr(String locAddr) {
        this.locAddr = locAddr;
    }

    public String getLocRoadAddr() {
        return locRoadAddr;
    }

    public void setLocRoadAddr(String locRoadAddr) {
        this.locRoadAddr = locRoadAddr;
    }

    public String getLocTel() {
        return locTel;
    }

    public void setLocTel(String locTel) {
        this.locTel = locTel;
    }

    public String getLocLat() {
        return locLat;
    }

    public void setLocLat(String locLat) {
        this.locLat = locLat;
    }

    public String getLocLng() {
        return locLng;
    }

    public void setLocLng(String locLng) {
        this.locLng = locLng;
    }

    public int getLocRegionCode() {
        return locRegionCode;
    }

    public void setLocRegionCode(int locRegionCode) {
        this.locRegionCode = locRegionCode;
    }
}
